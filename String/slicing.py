# String Slicing = Array Range

a = 'Today Is A Good And Happy Day'

print(a[ 2 : 7])
#Output : day I
print(a[ 2 : 3])
#Output : d
print(a[  : 3])
#Output : Tod
print(a[ 2 : ])
#Output : day Is A Good And Happy Day
print(a[ : ])
#Output : Today Is A Good And Happy Day
print(a[ -4 : -1])
#Output :  Da
